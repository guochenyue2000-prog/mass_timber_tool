"""
Glulam Beam Selection Tables — CWC Wood Design Manual (2017), pp. 68–73.

Table structure:
    GLULAM_BEAM_TABLES[width_mm][depth_mm][species][grade] = {
        "Mr_prime_kNm"  : float,  # M'r  — unadjusted factored moment resistance, kN·m
        "Vr_kN"         : float,  # V_r  — factored shear resistance (simplified, volume < 2 m³), kN
        "WrL018"        : float,  # W_r·L^0.18 — volume-based shear product from table, kN·m^0.18
        "EsI_kNm2"      : float,  # E_s I — bending stiffness, kN·m²  (converted from ×10⁹ N·mm²)
    }

Unit conversion: 1 × 10⁹ N·mm² = 1 kN·m²  (exact)

Species keys  : "Spruce-Pine", "D.Fir-L"
Grade keys    : "20f-E", "24f-E"
  Note: Spruce-Pine is only available in 20f-E grade.
  Note: All values also apply to EX grades (Table note 1).

Source: CWC Wood Design Manual Panel Selection Tables, Bending Members chapter.
"""

from typing import Dict, Any

# ---------------------------------------------------------------------------
# Type alias for a single beam entry
# ---------------------------------------------------------------------------
_Entry = Dict[str, float]
_Table = Dict[int, Dict[int, Dict[str, Dict[str, _Entry]]]]


def _e(mr: float, vr: float, wrl018: float, esi: float) -> _Entry:
    """Helper to build a table entry. EsI supplied as ×10⁹ N·mm², stored as kN·m²."""
    return {
        "Mr_prime_kNm": mr,       # kN·m — M'r from table
        "Vr_kN":        vr,       # kN   — Vr (simplified shear, volume < 2 m³)
        "WrL018":       wrl018,   # kN·m^0.18 — Wr·L^0.18 product
        "EsI_kNm2":     esi,      # kN·m² — bending stiffness (1×10⁹ N·mm² = 1 kN·m²)
    }


